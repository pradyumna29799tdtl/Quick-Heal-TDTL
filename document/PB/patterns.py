import re
# Aadhaar pattern
aadhaar_pattern = re.compile(
    r'''
    (?<!\d)                # No digit before
    (
        (?:[०-९]{4}[-\s]?[०-९]{4}[-\s]?[०-९]{4}) |    # Hindi
        (?:[௦-௯]{4}[-\s]?[௦-௯]{4}[-\s]?[௦-௯]{4}) |    # Tamil
        (?:[౦-౯]{4}[-\s]?[౦-౯]{4}[-\s]?[౦-౯]{4}) |    # Telugu
        (?:[0-9]{4}[-\s]?[0-9]{4}[-\s]?[0-9]{4})      # English
    )
    (?!\d)                # No digit after
    ''',
    re.VERBOSE
)# PAN pattern
