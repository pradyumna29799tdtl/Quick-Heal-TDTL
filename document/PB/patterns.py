# Mobile number pattern
mobile_number_pattern = re.compile(
    r'''
    (?:(?:\+91|91|0)?[\s-]?[789]\d{9})                 # Indian numbers with optional country code and hyphens or spaces
    | (?:\+?\d{1,3}[\s-]?)?[789]\d{9}                  # International numbers with optional country code and separators
    | (?:[\u0966-\u096F\u09E6-\u09EF\u0AE6-\u0AEF\u0A66-\u0A6F\u0CE6-\u0CEF\u0D66-\u0D6F\u0BE6-\u0BEF\u0C66-\u0C6F\u0B66-\u0B6F]{10})  # Numbers in Indic scripts
    | \+९१\s\d{5}\s\d{5}                              # Hindi numbers with international code and spaces
    | \+?\d{2,3}\s?\d{10}                              # Numbers with optional international code and space
    | \(\d{3}\)\s?\d{3}-\d{4}                          # US-style numbers with area code in parentheses
    ''',
    re.VERBOSE
)
